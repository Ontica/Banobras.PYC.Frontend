export function assertPermissionsCoverage() {
  const allPermissions = getAllPermissions();

  const classifiedPermissions = [...BUDGET_PERMISSIONS, ...PAYMENTS_PERMISSIONS];

  const missingPermissions = allPermissions.filter(x => !classifiedPermissions.includes(x as PERMISSIONS));

  if (missingPermissions.length > 0) {
    console.warn(`Unclassified permissions found: ${missingPermissions.join(', ')}`, missingPermissions);
  }
}
